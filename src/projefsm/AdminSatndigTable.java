    private void jButton1showActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1showActionPerformed
        // TODO add your handling code here:
        DefaultTableModel model = new DefaultTableModel();
        model.setRowCount(0); // show button bir daha basarsak satirlarin sayisi 0 olacak sonra sadece database tabledaki bilgileri yani satirlari yazdiracak
        model = (DefaultTableModel)jTable1.getModel();

        try {
            model.setRowCount(0);
            String sql = "SELECT * FROM mysql.standingtable ";
            Connection con = MakeCon.MakeConnection();
            ps = con.prepareStatement(sql);
            rst = ps.executeQuery();

            while (rst.next()) {
                model.addRow(new Object[]{rst.getString(1) , rst.getString(2) , rst.getString(3)});
            }

        } catch (SQLException ex) {

            System.out.println(ex.getMessage());

        }
    }//GEN-LAST:event_jButton1showActionPerformed

