    private void jButton1SearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1SearchActionPerformed
        // TODO add your handling code here:
       DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        boolean found = false;

        for (int i = 0; i < model.getRowCount(); i++) {
            for (int j = 0; j < model.getColumnCount(); j++) {
                if (jTextField1Search.getText().equals(model.getValueAt(i, j).toString())) {
                    model.setValueAt(model.getValueAt(i, j).toString() + "***", i, j);
                    jLabel2search.setText(""); 
                    JOptionPane.showMessageDialog(null, "Founded, We have set *** sign aside the channel that you are looking for");
                    found = true;
                    break; 
                }
            }
            if (found) {
                break; 
            }
        }

        if (!found) {
            jLabel2search.setText("Not founded");
        }
    }//GEN-LAST:event_jButton1SearchActionPerformed

